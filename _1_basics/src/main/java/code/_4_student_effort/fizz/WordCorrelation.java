package code._4_student_effort.fizz;

public class WordCorrelation {
    private Integer nr;
    private String name;

    public WordCorrelation(int nr, String name){
        this.nr=nr;
        this.name=name;
    }

    public Integer getNr() {
        return nr;
    }


    public String getName() {
        return name;
    }

}
